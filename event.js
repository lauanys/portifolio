
let css = document.querySelector('#css');
let textCss = document.querySelector('.css');
//event hover
css.addEventListener('mouseenter', function(){
    textCss.style.display = 'flex';
    css.style.backgroundColor = 'white';
});
css.addEventListener('mouseout', function(){
textCss.style.display = 'none';
css.style.backgroundColor = 'rgba(255, 255, 255, 0)';
});
//event click
css.addEventListener('click', function(){
if(textCss.style.display === 'none'){
    textCss.style.display = 'flex';
    css.style.backgroundColor = 'white';

} else{
    textCss.style.display = 'none';
    css.style.backgroundColor = 'rgba(255, 255, 255, 0)';
}
});

 


